def count_words_in_silence(text: str) -> dict:
    """–ü–æ–¥—Å—á—ë—Ç —Å–ª–æ–≤ –≤ —Ç–∏—à–∏–Ω–µ - –∫–∞–∂–¥—ã–π –ø–æ–ª—É—á–∞–µ—Ç —Å–≤–æ—ë –º–µ—Å—Ç–æ –≤ —Å–ª–æ–≤–∞—Ä–µ."""
    
    words = text.lower().split()
    word_count = {}
    
    for word in words:
        # –û—á–∏—â–∞–µ–º –æ—Ç –∑–Ω–∞–∫–æ–≤ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è
        clean_word = ''.join(char for char in word if char.isalnum())
        if clean_word:
            word_count[clean_word] = word_count.get(clean_word, 0) + 1
    
    return word_count

